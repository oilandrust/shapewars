bool isWalkable(Level* level, const Vec3& pos)
{
    if (pos.x < 0 || pos.x > level->width)
        return false;

    if (pos.y < 0 || pos.y > level->height)
        return false;

    uint32 i = (uint32)pos.x;
    uint32 j = level->height - (uint32)pos.y;
    return level->tiles[i + j * level->width] == 0;
}
